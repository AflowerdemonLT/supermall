<template>
  <div class="tab-control">
    <!-- 只是文字的情况下没有必要用插槽 -->
    <div
      v-for="(item, index) in titles"
      :key="index"
      @click="itemClick(index)"
      class="tab-control-item"
      :class="{ active: index == currentIndex }"
    >
      <span>{{ item }}</span>
    </div>
  </div>
</template>

<script>
export default {
  name: "TabControl",
  props: {
    titles: {
      type: Array,
      default() {
        return [];
      },
    },
  },
  data() {
    return {
      // 记录谁被选中了
      currentIndex: 0,
    };
  },
  methods: {
    itemClick(index) {
      this.currentIndex = index;
      this.$emit("tabClick", index);
    },
  },
};
</script>

